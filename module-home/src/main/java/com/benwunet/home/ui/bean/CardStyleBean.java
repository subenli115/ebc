package com.benwunet.home.ui.bean;

public class CardStyleBean {

    /**
     * styleId : STYLE_TRADITION
     * styleName : 传统名片版
     * styleThumbnail : http://2.jpg
     */

    private String styleId;
    private String styleName;
    private String styleThumbnail;

    public String getStyleId() {
        return styleId;
    }

    public void setStyleId(String styleId) {
        this.styleId = styleId;
    }

    public String getStyleName() {
        return styleName;
    }

    public void setStyleName(String styleName) {
        this.styleName = styleName;
    }

    public String getStyleThumbnail() {
        return styleThumbnail;
    }

    public void setStyleThumbnail(String styleThumbnail) {
        this.styleThumbnail = styleThumbnail;
    }
}
